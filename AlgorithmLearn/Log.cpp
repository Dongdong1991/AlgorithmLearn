#include "Log.hpp"


void kStringPrintf(string s){
    printf("%s\n",s.c_str());
}

void kCharPrintf(char s){
    printf("%c\n",s);
}

void kIntPrintf(int value){
    printf("%d\n",value);
}

void kFloatPrintf(float value){
    printf("%f\n",value);
}
